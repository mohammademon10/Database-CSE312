--DBMS VS RDBMS
--DBMS: Database Management System
--RDBMS: Relational Database Management System

--DBMS: A DBMS is a software system that allows users to define, create, maintain, and control access to databases. It provides an interface for users to interact with the database and manage data. Examples of DBMS include Microsoft Access, FileMaker Pro, and SQLite.
--RDBMS: An RDBMS is a type of DBMS that organizes data into tables (relations) with rows and columns. It uses SQL (Structured Query Language) for managing and manipulating the data. RDBMS supports relationships between tables, which allows for more complex queries and data integrity. Examples of RDBMS include MySQL, Oracle, and SQL Server.

--Key Differences between DBMS and RDBMS:

--1. Data Structure: DBMS does not support a structured format for data storage, while
--   RDBMS organizes data into tables with rows and columns.

--2. Relationships: DBMS does not support relationships between data, while RDBMS
--   allows for relationships between tables, enabling complex queries and data integrity.

--3. SQL Support: DBMS may not support SQL, while RDBMS uses SQL for data
--   management and manipulation.

--4. Data Integrity: DBMS does not enforce data integrity, while RDBMS enforces
--   data integrity through constraints and relationships.

--5. Scalability: DBMS is generally suitable for smaller applications, while RDBMS
--   is designed to handle larger and more complex applications with high scalability.

--In summary, while both DBMS and RDBMS are used for managing databases, RDBMS offers more advanced features and capabilities for handling complex data relationships and ensuring data integrity, making it the preferred choice for most applications.